/* Returns [lat, lng] for a mission name, or null if unknown.
   Matches the longest key first to avoid ambiguous short substrings. */
window.getMissionCoords = function (name) {
  var keys = Object.keys(window.CITY_COORDS).sort(function (a, b) { return b.length - a.length; });
  for (var i = 0; i < keys.length; i++) {
    if (name.indexOf(keys[i]) !== -1) return window.CITY_COORDS[keys[i]];
  }
  return null;
};
